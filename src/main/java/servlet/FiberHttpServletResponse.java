    @Override
    public ServletOutputStream getOutputStream() throws IOException {
        final Condition cond = new SimpleConditionSynchronizer(null);
        final AtomicBoolean flag = new AtomicBoolean(false);

        ServletOutputStream w = response.getOutputStream();

        w.setWriteListener(new WriteListener() {

            @Override
            public void onWritePossible() throws IOException {

                flag.set(true);
                cond.signalAll();

            }

            @Override
            public void onError(Throwable throwable) {
                flag.set(true);
                cond.signalAll();

            }
        });
        Object token = cond.register();
        try {
            for (int i = 0; !flag.get(); i++)
                cond.await(i);
        } catch (InterruptedException e) {
            e.printStackTrace();
        } catch (SuspendExecution suspendExecution) {
            suspendExecution.printStackTrace();
        } finally {
            cond.unregister(token);
        }

        return w;
    }

